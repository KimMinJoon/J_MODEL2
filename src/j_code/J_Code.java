package j_code;

public class J_Code {
	private String c_major;
	private String c_minor;
	private String c_value;

	public String getC_major() {
		return c_major;
	}

	public void setC_major(String c_major) {
		this.c_major = c_major;
	}

	public String getC_minor() {
		return c_minor;
	}

	public void setC_minor(String c_minor) {
		this.c_minor = c_minor;
	}

	public String getC_value() {
		return c_value;
	}

	public void setC_value(String c_value) {
		this.c_value = c_value;
	}
}
